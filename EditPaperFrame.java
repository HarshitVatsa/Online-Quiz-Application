    private void btnEditQueActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditQueActionPerformed
        String sub=jcbChooseSub.getSelectedItem().toString();
        String examid=jcbChooseExamId.getSelectedItem().toString();
        int ans;
        ans=JOptionPane.showConfirmDialog(null,"You have choosen\""+sub+"\"and\""+examid+"\"exam!\nIs this ok?","Confirmation!",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(ans==JOptionPane.YES_OPTION){
                String examId=jcbChooseExamId.getSelectedItem().toString();
                try{
                int totalQue=ExamDao.getQuestionCountByExam(examId);
                ExamPojo newExam=new ExamPojo(examId,sub,totalQue);
                EditQuestionsFrame edit=new EditQuestionsFrame(newExam);
                edit.setVisible(true);
                this.dispose();
                }
                catch(SQLException e){
                    JOptionPane.showConfirmDialog(null,"Error while connecting to DB!","Exception!",JOptionPane.ERROR_MESSAGE);
                    e.printStackTrace();
                }
            }
            
    }//GEN-LAST:event_btnEditQueActionPerformed

    private void jcbChooseSubItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbChooseSubItemStateChanged
        String subject=jcbChooseSub.getSelectedItem().toString();
        try{
            ArrayList<String>examId=ExamDao.getExam(subject);
            jcbChooseExamId.removeAllItems();
            for(String exid:examId){
                jcbChooseExamId.addItem(exid);
            }
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,"Error while connecting to DB!","Error!",JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_jcbChooseSubItemStateChanged

