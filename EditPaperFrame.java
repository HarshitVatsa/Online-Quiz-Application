public void loadSubject(){
    try{
        ArrayList<String>subList=ExamDao.getSubject();
        if(subList.isEmpty()==true){
            JOptionPane.showMessageDialog(null,"No paper has been selected!","No Records!",JOptionPane.INFORMATION_MESSAGE);
            
        }
        else
        {
            jcbChooseSub.removeAllItems();
            for(String sub:subList){
                jcbChooseSub.addItem(sub);
            }
        }
    }
    catch(SQLException e)
    {
        JOptionPane.showMessageDialog(null,"Error while connecting to DB!","Error!",JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
}
    
